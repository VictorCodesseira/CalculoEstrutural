function out = generateA()
  syms A 
  
  syms z kappa

  A(1,1) = 1;
  A(1,2) = z;
  A(1,3) = z^2;
  A(1,4) = z^3;
  A(1,5) = z^4;
  A(1,6) = z^5;

  A(2,10) = 1;
  A(2,11) = z;
  A(2,12) = z^2;
  A(2,13) = z^3;
  A(2,14) = z^4;
  A(2,15) = z^5;

  A(3,3) = (1/3)*(kappa*z^3);
  A(3,4) = (1/4)*(kappa*z^4);
  A(3,5) = (1/5)*(kappa*z^5);
  A(3,6) = (1/6)*(kappa*z^6);
  A(3,7) = 1;
  A(3,8) = z;
  A(3,9) = z^2;

  A(4,11) = -1;
  A(4,12) = -2*z;
  A(4,13) = -3*z^2;
  A(4,14) = -4*z^3;
  A(4,15) = -5*z^4;

  A(5,2) = 1;
  A(5,3) = (1/3)*(kappa^2*z^3) + 2*z;
  A(5,4) = (1/4)*(kappa^2*z^4) + 3*z^2;
  A(5,5) = (1/5)*(kappa^2*z^5) + 4*z^3;
  A(5,6) = (1/6)*(kappa^2*z^6) + 5*z^4;
  A(5,7) = kappa;
  A(5,8) = kappa*z;
  A(5,9) = kappa*z^2;

  A(6,13) = -kappa*z^3;
  A(6,14) = -kappa*z^4;
  A(6,15) = -kappa*z^5;
  A(6,16) = 1;
  A(6,17) = z;
  A(6,18) = z^2;

  out = A;
end

